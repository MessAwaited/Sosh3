export function TheoryPage() {
  const { completedLessonIds } = useProgress();
  const [openId, setOpenId] = useState<string | null>(null);

  return (
    <div className={styles.page}>
      <section className={styles.hero}>
        <motion.h1
          className={styles.heroTitle}
          initial={{ opacity: 0, y: 12 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.4 }}
        >
          Теоретические материалы
        </motion.h1>
        <motion.p
          className={styles.heroSub}
          initial={{ opacity: 0, y: 8 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ delay: 0.08, duration: 0.35 }}
        >
          В этом разделе собраны материалы по Python и базовым алгоритмам: ключевые понятия, пояснения к темам, примеры кода и рекомендации для решения задач. Изучайте конспекты перед уроками, повторяйте сложные места и переходите к практике, когда тема станет понятной.
        </motion.p>
      </section>

      <motion.section
        className={styles.section}
        initial={{ opacity: 0, y: 12 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.3 }}
      >
        <motion.ul
          className={styles.list}
          initial="hidden"
          animate="visible"
          variants={{
            visible: { transition: { staggerChildren: 0.05 } },
            hidden: {},
          }}
        >
          <AnimatePresence>
            {lessons.map((lesson, index) => {
              const section = theoryByLessonId[lesson.id];
              const previousLesson = index > 0 ? lessons[index - 1] : null;
              const isLocked = previousLesson != null && !completedLessonIds.has(previousLesson.id);
              const isOpen = openId === lesson.id;

              return (
                <motion.li
                  key={lesson.id}
                  className={`${styles.card} ${isLocked ? styles.locked : ''}`}
                  variants={{
                    visible: { opacity: 1, y: 0 },
                    hidden: { opacity: 0, y: 16 },
                  }}
                  transition={{ duration: 0.3 }}
                >
                  <button
                    type="button"
                    className={styles.cardHead}
                    onClick={() => {
                      if (isLocked) return;
                      setOpenId(isOpen ? null : lesson.id);
                    }}
                    disabled={isLocked}
                  >
                    <AlgorithmIcon name={lesson.algorithmIcon} locked={isLocked} />
                    <div className={styles.cardContent}>
                      <h3 className={styles.cardTitle}>{lesson.title}</h3>
                      <p className={styles.cardDesc}>{section.content}</p>
                    </div>
                    <div className={styles.cardSide}>
                      {isLocked ? (
                        <Lock size={18} className={styles.lockIcon} />
                      ) : isOpen ? (
                        <ChevronUp size={20} />
                      ) : (
                        <ChevronDown size={20} />
                      )}
                    </div>
                  </button>
                  <AnimatePresence>
                    {!isLocked && isOpen && (
                      <motion.div
                        className={styles.cardBody}
                        initial={{ height: 0, opacity: 0 }}
                        animate={{ height: 'auto', opacity: 1 }}
                        exit={{ height: 0, opacity: 0 }}
                        transition={{ duration: 0.25 }}
                      >
                        <p className={styles.content}>{section.details}</p>
                        {section.code && (
                          <pre className={styles.code}>
                            <code>{section.code}</code>
                          </pre>
                        )}
                        <div className={styles.cardFooter}>
                          <Link to={`/student/lesson/${lesson.id}`} className={styles.goLessonBtn}>
                            Перейти к курсу по теме блока
                            <ArrowRight size={16} />
                          </Link>
                        </div>
                      </motion.div>
                    )}
                  </AnimatePresence>
                </motion.li>
              );
            })}
          </AnimatePresence>
        </motion.ul>
      </motion.section>
    </div>
  );
}
